namespace Tunneln.items.connections
{
    /// <summary>
    /// このカスタム コントロールを XAML ファイルで使用するには、手順 1a または 1b の後、手順 2 に従います。
    ///
    /// 手順 1a) 現在のプロジェクトに存在する XAML ファイルでこのカスタム コントロールを使用する場合
    /// この XmlNamespace 属性を使用場所であるマークアップ ファイルのルート要素に
    /// 追加します:
    ///
    ///     xmlns:MyNamespace="clr-namespace:Tunneln.items.connections"
    ///
    ///
    /// 手順 1b) 異なるプロジェクトに存在する XAML ファイルでこのカスタム コントロールを使用する場合
    /// この XmlNamespace 属性を使用場所であるマークアップ ファイルのルート要素に
    /// 追加します:
    ///
    ///     xmlns:MyNamespace="clr-namespace:Tunneln.items.connections;assembly=Tunneln.items.connections"
    ///
    /// また、XAML ファイルのあるプロジェクトからこのプロジェクトへのプロジェクト参照を追加し、
    /// リビルドして、コンパイル エラーを防ぐ必要があります:
    ///
    ///     ソリューション エクスプローラーで対象のプロジェクトを右クリックし、
    ///     [参照の追加] の [プロジェクト] を選択してから、このプロジェクトを参照し、選択します。
    ///
    ///
    /// 手順 2)
    /// コントロールを XAML ファイルで使用します。
    ///
    ///     <MyNamespace:LocalPortForward/>
    ///
    /// </summary>
    public class LocalPortForward : CommonPannel
    {
        static LocalPortForward()
        {
            DefaultStyleKeyProperty.OverrideMetadata(typeof(LocalPortForward), new FrameworkPropertyMetadata(typeof(LocalPortForward)));
        }

        public LocalPortForward()
        {
            this.CreateInitFlow();
        }

        public void CreateInitFlow()
        {
            this.Children.Add(new groups.YourComputer("local\r\nhost"));
            this.Children.Add(new groups.RemoteHost("jump\r\nhost"));
            this.Children.Add(new groups.RemoteHost("remote\r\nhost"));
        }

    }
}
